package dto;

public class UserDto {
	Long id;
	String login;
	String password;
	String name;
	String email;
	
	public Long getId() {
		return id;
	}
	public String getLogin() {
		return login;
	}
	public String getPassword() {
		return password;
	}
	public String getName() {
		return name;
	}
	public String getEmail() {
		return email;
	}
}
